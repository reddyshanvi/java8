package com.Eshanvi.chapter17;
import java.util.*;
public class HashSetDemo {

	public static void main(String[] args) {
		HashSet<String> hs=new HashSet<String>();//here we are creating HashSet of type String
		hs.add("D");//here we are adding elements to hashset
		hs.add("B");
		hs.add("H");
		hs.add("F");
		hs.add("C");
		System.out.println(hs);//here we are printing the elements of hashset ,in hashset elements may print sorted order or may not print in sorted order

	}

}
